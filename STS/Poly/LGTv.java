package poly;

public class LGTv {
	public void turnOn() {
		System.out.println("LGTV ----- 파워온!");
	}

	public void turnOff() {
		System.out.println("LGTV ----- 파워다운!");
	}

	public void soundUp() {
		System.out.println("LGTV ----- 소리 업!");
	}

	public void soundDown() {
		System.out.println("LGTV ----- 소리 다운!");
	}
	
	
}
