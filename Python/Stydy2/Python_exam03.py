# 숫자를 입력받아 무지개 일곱 빛 중 하나를 출력하는 프로그램을 작성하시오.
num = int(input("1~7 사이의 숫자를 입력하세요: "))

if num == 1:
    print("빨강")
elif num == 2:
    print("주황")
elif num == 3:
    print("노랑")
elif num == 4:
    print("초록")
elif num == 5:
    print("파랑")
elif num == 6:
    print("남색")
elif num == 7:
    print("보라")
else:
    print("1~7 사이의 숫자를 입력하세요.")
# 무지개 색 출력 프로그램
# 1: 빨강, 2: 주황, 3: 노랑, 4: 초록, 5: 파랑, 6: 남색, 7: 보라